require 'set'

INPUT = <<~TEXT
      47|53
      97|13
      97|61
      97|47
      75|29
      61|13
      75|53
      29|13
      97|29
      53|29
      61|53
      97|53
      61|29
      47|13
      75|47
      97|75
      47|61
      75|61
      47|29
      75|13
      53|13

      75,47,61,53,29
      97,61,53,29,13
      75,29,13
      75,97,47,61,53
      61,13,29
      97,13,75,29,47
      TEXT

SECTIONS = INPUT.split("\n\n")
RULES = SECTIONS.first.split
ORD = RULES.map { |rule| rule.split("|") }

hash = {}

LEFT = ORD.map(&:first).to_set
LEFT.each do |page|
  hash[page.to_i] = ORD.select { |rule| rule.first == page }.map(&:last).map(&:to_i)
end

puts hash

